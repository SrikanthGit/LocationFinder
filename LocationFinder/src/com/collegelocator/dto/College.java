package com.collegelocator.dto;

public class College {

	private String latitude;
	private String longitude;
	private String name;
	private String address;
	private String details;
	private String specialNote;
	private String code;
	private String website;
	private String ranking;
	private String distance;
	private Address addressMap;
	private Contact contactDetails;
	/**
	 * @return the latitude
	 */
	public String getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	/**
	 * @return the longitude
	 */
	public String getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the details
	 */
	public String getDetails() {
		return details;
	}
	/**
	 * @param details the details to set
	 */
	public void setDetails(String details) {
		this.details = details;
	}
	/**
	 * @return the specialNote
	 */
	public String getSpecialNote() {
		return specialNote;
	}
	/**
	 * @param specialNote the specialNote to set
	 */
	public void setSpecialNote(String specialNote) {
		this.specialNote = specialNote;
	}
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return the website
	 */
	public String getWebsite() {
		return website;
	}
	/**
	 * @param website the website to set
	 */
	public void setWebsite(String website) {
		this.website = website;
	}
	/**
	 * @return the ranking
	 */
	public String getRanking() {
		return ranking;
	}
	/**
	 * @param ranking the ranking to set
	 */
	public void setRanking(String ranking) {
		this.ranking = ranking;
	}
	/**
	 * @return the distance
	 */
	public String getDistance() {
		return distance;
	}
	/**
	 * @param distance the distance to set
	 */
	public void setDistance(String distance) {
		this.distance = distance;
	}
	/**
	 * @return the addressMap
	 */
	public Address getAddressMap() {
		return addressMap;
	}
	/**
	 * @param addressMap the addressMap to set
	 */
	public void setAddressMap(Address addressMap) {
		this.addressMap = addressMap;
	}
	/**
	 * @return the contactDetails
	 */
	public Contact getContactDetails() {
		return contactDetails;
	}
	/**
	 * @param contactDetails the contactDetails to set
	 */
	public void setContactDetails(Contact contactDetails) {
		this.contactDetails = contactDetails;
	}
	
	
}
